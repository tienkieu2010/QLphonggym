package com.example.demo1.Class;

public class Goitap {
    public String tengoi;
    public int Gia;

    public Goitap() {
    }

    public Goitap(String tengoi, int Gia) {
        this.tengoi = tengoi;
        this.Gia = Gia;
    }

    public String getTengoi() {
        return tengoi;
    }

    public void setTengoi(String tengoi) {
        this.tengoi = tengoi;
    }

    public int getGia() {
        return Gia;
    }

    public void setGia(int Gia) {
        this.Gia = Gia;
    }
}
